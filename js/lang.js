// 用户浏览器语言
// ['ENPH','ENMY','TH','TW','ID']
//  菲律宾、马来西亚、泰国、台湾、印尼
// ['en-ph','en-my','th-th','zh-tw','id-id']
let LANGUAGE = ''
let userLanguage = navigator.language || navigator.userLanguage;
console.log(userLanguage.toLowerCase(),'userLanguage============');
switch (userLanguage.toLowerCase()) {
	case 'zh-tw':
		LANGUAGE = 'TW'
		break
	case 'id':
		LANGUAGE = 'ID'
		break
	case 'id-id':
		LANGUAGE = 'ID'
		break
	case 'th':
		LANGUAGE = 'TH'
		break
	case 'th-th':
		LANGUAGE = 'TH'
		break	
	case 'ms':
		LANGUAGE = 'ENMY'
		break	
	case 'en-my':
		LANGUAGE = 'ENMY'
		break
	case 'fil':
		LANGUAGE = 'ENPH'
		break	
	case 'en-ph':
		LANGUAGE = 'ENPH'
		break
	default:
		LANGUAGE = 'ENMY'
		break;
}

// 当前页面语言
const currentLan = new URLSearchParams(window.location.search).get('language') || '';

if (currentLan !== LANGUAGE && !sessionStorage.getItem('language')) {
	changeLang(LANGUAGE);
}